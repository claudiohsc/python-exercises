n1 = int(input('Digite um número:'))
n2 = int(input('Digite outro número: '))
s = n1 + n2
d = n1 / n2
m = n1 * n2
di = n1 // n2
e = n1 ** n2
print('A soma é {}, a divisão é {}'.format(s, d), end='')
print(', o produto é {}, a divisão inteira é {} e a potência é {}.'.format(m, di, e))
print('Á disposição!')
